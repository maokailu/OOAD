package entity;


public class Guitar {
	public String guitarId;
	public String guitarName;
	public String guitarPrice;
	public String guitarType;
	public String getGuitarId() {
		return guitarId;
	}
	public void setGuitarId(String guitarId) {
		this.guitarId = guitarId;
	}
	public String getGuitarName() {
		return guitarName;
	}
	public void setGuitarName(String guitarName) {
		this.guitarName = guitarName;
	}
	public String getGuitarPrice() {
		return guitarPrice;
	}
	public void setGuitarPrice(String guitarPrice) {
		this.guitarPrice = guitarPrice;
	}
	public String getGuitarType() {
		return guitarType;
	}
	public void setGuitarType(String guitarType) {
		this.guitarType = guitarType;
	}
	
}
